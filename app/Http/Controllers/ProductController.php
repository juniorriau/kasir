     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $angka = range(0, 9);
        shuffle($angka);
        $string = array_rand($angka, 8);
        $kode = implode("", $string);

        Product::create([
            'kode' => $kode,
            'productname' => $request->productname,
            'harga' => $request->harga,
        ]);

        return redirect()->back()->with('sukses', 'Product berhasil di tambahkan');
    }

